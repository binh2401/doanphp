<script>
    document.addEventListener('DOMContentLoaded', function() {
        const cartButtons = document.querySelectorAll('.btn-cart');
        cartButtons.forEach(button => {
            button.addEventListener('click', function(event) {
                event.preventDefault();
                const productId = this.getAttribute('href').split('id=')[1];
                const quantity = this.closest('.product-item').querySelector('.quantity').value;

                fetch('cart.php?action=add', {
                        method: 'POST',
                        headers: {
                            'Content-Type': 'application/json'
                        },
                        body: JSON.stringify({
                            id: productId,
                            quantity: quantity
                        })
                    })
                    .then(response => response.json())
                    .then(data => {
                        if (data.success) {
                            updateCart(data.cart);
                        } else {
                            alert('Failed to add product to cart.');
                        }
                    })
                    .catch(error => console.error('Error:', error));
            });
        });

        function updateCart(cart) {
            const cartList = document.querySelector('.offcanvas-body .list-group');
            cartList.innerHTML = '';
            let total = 0;

            cart.forEach(item => {
                const listItem = document.createElement('li');
                listItem.classList.add('list-group-item', 'd-flex', 'justify-content-between', 'lh-sm');
                listItem.innerHTML = `
                <div>
                    <h6 class="my-0">${item.name}</h6>
                    <small class="text-body-secondary">${item.description}</small>
                </div>
                <span class="text-body-secondary">$${item.price}</span>
            `;
                cartList.appendChild(listItem);
                total += item.price * item.quantity;
            });

            const totalItem = document.createElement('li');
            totalItem.classList.add('list-group-item', 'd-flex', 'justify-content-between');
            totalItem.innerHTML = `
            <span>Total (USD)</span>
            <strong>$${total}</strong>
        `;
            cartList.appendChild(totalItem);

            document.querySelector('.offcanvas-body .badge').textContent = cart.length;
        }
    });
</script>